package DataTypes;

public class NewPrimiCode {

    public static void main() {

        byte num1=10;
        System.out.println(num1);

        short num2=1000;
        System.out.println(num2);

        int num3=500000;
        System.out.println(num3);

        long num4=1000000;
        System.out.println(num4);

        float num5=1.59293723123f;   //f-mandatory
        System.out.println(num5);

        double num6=10.877464758d;
        System.out.println(num6);

        char grade='B';
        System.out.println(grade);

        char number='8';
        System.out.println(number);

        boolean Rcb_win=true;
        System.out.println(Rcb_win);
    }
}
